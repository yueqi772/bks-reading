/**
 * 调用 AI 接口进行书籍解读（非流式，返回完整内容）
 * 实际项目建议使用流式接口，此处为兼容小程序网络限制的备选方案
 */
function readBook(bookTitle, mode, callbacks) {
  const { onStart, onChunk, onComplete, onError } = callbacks;

  if (onStart) onStart();

  // 构造请求体
  const requestBody = {
    model: AI_CONFIG.model,
    messages: [
      { role: 'system', content: SYSTEM_PROMPT },
      { role: 'user', content: buildReadPrompt(bookTitle, mode) }
    ],
    max_tokens: 8000,
    temperature: 0.7,
    stream: false
  };

  wx.request({
    url: `${AI_CONFIG.baseUrl}/chat/completions`,
    method: 'POST',
    header: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${AI_CONFIG.apiKey}`
    },
    data: requestBody,
    timeout: 120000, // 120秒超时
    success(res) {
      if (res.statusCode === 200 && res.data && res.data.choices && res.data.choices[0]) {
        const content = res.data.choices[0].message.content;
        if (onChunk) onChunk(content);
        if (onComplete) onComplete(content);
      } else {
        const errMsg = res.data?.error?.message || `接口错误 (${res.statusCode})`;
        if (onError) onError(new Error(errMsg));
      }
    },
    fail(err) {
      if (onError) onError(new Error(err.errMsg || '网络请求失败'));
    }
  });
}

